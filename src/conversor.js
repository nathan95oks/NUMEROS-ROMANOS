function convertir(a){
    if(a===1){
    return "I";
    };
    if(a===2){
        return "II";
    };
    if(a===3){
        return "III";
    };
    if(a===4){
        return "IV";
    };
    if(a===5){
        return "V";
    };
    if(a===6){
        return "VI";
    };
    if(a===7){
        return "VII";
    };
    if(a===8){
        return "VIII";
    };
    if(a===9){
        return "IX";
    };
};

export default convertir;